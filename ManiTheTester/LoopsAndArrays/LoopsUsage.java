package com.ManiTheTester.LoopsAndArrays;

public class LoopsUsage {
    public static void main(String[] args) {

        //Loops and Arrays

        String[] names = {"James", "Mani", "King", "Sophia", "Alex"};

        for(int i=0; i<names.length; i++){
            System.out.println(names[i]);
        }

    }
}
